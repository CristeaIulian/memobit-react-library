// Generates src/components/Icon/iconNames.ts — an explicit IconName union
// derived from the keys of `iconMap` in iconMap.ts.
//
// Why: typing IconName as `keyof typeof iconMap` forced the bundled
// index.d.ts to embed the entire 475-entry iconMap object type, which
// dominated DTS build time. An explicit union keeps IconName flat.
//
// Run after editing iconMap.ts: `npm run generate:icon-names`

import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const here = dirname(fileURLToPath(import.meta.url));
const iconMapPath = join(here, '../src/components/Icon/iconMap.ts');
const outPath = join(here, '../src/components/Icon/iconNames.ts');

const source = readFileSync(iconMapPath, 'utf8');
const lines = source.split(/\r?\n/);

const startIndex = lines.findIndex((line) => line.includes('iconMap'));
if (startIndex === -1) {
    throw new Error('Could not locate the iconMap declaration in iconMap.ts');
}

const keys = [];
for (let i = startIndex + 1; i < lines.length; i++) {
    const line = lines[i];
    if (/^\s*}/.test(line)) break;

    const quoted = line.match(/^\s*'([^']+)'\s*:/);
    const unquoted = line.match(/^\s*([A-Za-z0-9_$]+)\s*:/);
    const shorthand = line.match(/^\s*([A-Za-z0-9_$]+)\s*,\s*$/);

    if (quoted) keys.push(quoted[1]);
    else if (unquoted) keys.push(unquoted[1]);
    else if (shorthand) keys.push(shorthand[1]);
}

const unique = [...new Set(keys)].sort();
if (unique.length === 0) {
    throw new Error('No icon keys were extracted — check the iconMap format');
}

const union = unique.map((key) => `    | '${key}'`).join('\n');
const output = `// AUTO-GENERATED by scripts/generate-icon-names.mjs — do not edit by hand.
// Run \`npm run generate:icon-names\` after changing iconMap.ts.

export type IconName =
${union};
`;

writeFileSync(outPath, output);
console.log(`Wrote ${unique.length} icon names to ${outPath}`);
